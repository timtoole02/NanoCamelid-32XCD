def tile(rows):
    """5x7 glyph -> 32-byte Genesis 4bpp tile (8x8, color 1, 1px pad)."""
    out = bytearray(32)
    for r in range(7):
        bits = rows[r]
        for c in range(8):
            on = c < 5 and (bits >> (4 - c)) & 1
            if on:
                out[r * 4 + c // 2] |= (1 << (4 if c % 2 == 0 else 0))
    return bytes(out)


def main():
    blank = tile(G[' '])
    font = b"".join(tile(G[chr(a)]) if chr(a) in G else blank
                    for a in range(32, 96))
    open(sys.argv[1], "wb").write(font)
    print(f"{sys.argv[1]}: {len(font)} bytes, {len(font)//32} tiles")


if __name__ == "__main__":
    main()
